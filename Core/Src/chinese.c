void LCD_Show_Chinese(u8 Line, u16 Column, u16 Color, u16 BkColor)
{
    u8 i, k, byte_index;
    u16 x, y;
    u8 temp;

    u16 StartCol = Column; 

    // 遍历 6 个汉字
    for (k = 0; k < 6; k++) 
    {
        u16 char_start_y = StartCol - (k * 24); 
        // 遍历 72 个字节
        for (byte_index = 0; byte_index < 72; byte_index++) 
        {
            temp = Font_Chinese_24x24[k * 72 + byte_index];
            x = Line + (byte_index / 3); 
            u16 pixel_start_y = char_start_y - ((byte_index % 3) * 8);
            for (i = 0; i < 8; i++) 
            {
                y = pixel_start_y - i; 
                LCD_SetCursor(x, y);     
                LCD_WriteRAM_Prepare();
                if (temp & 0x80)
                    LCD_WriteRAM(Color);
                else
                    LCD_WriteRAM(BkColor);
                temp <<= 1;
            }
        }
    }
}
